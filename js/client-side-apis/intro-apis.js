// API
// APIs are constructors made available in programming languages to
// allow developers to create complex functionality more easily.
// They abstract more complex code away from you, providing some easier syntax
// to use in its place.

// Difference between a library and a framework
// The key difference between a library and a framework is "Inversion of Control"
// When calling a method from a library, the developer is in control.
// With a framework, the control is inverted. The framework calls the developers code.
